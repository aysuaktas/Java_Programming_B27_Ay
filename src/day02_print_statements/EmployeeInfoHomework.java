package day02_print_statements;

public class EmployeeInfoHomework {

    /*
    Print:
        Company name:    Google
        Employee:        Khalid
        ID:              12345
        Job Title:       SDET

     */

    public static void main(String[] args) {

        System.out.println("Company name:\tGoogle");
        System.out.println("Employee:\t\tKhalid");
        System.out.println("ID\t\t\t\t12345");
        System.out.println("Job Title:\t\tSDET"); // First solution

        System.out.println("\nCompany name:\tGoogle\nEmployee\t\tKhalid\nID:\t\t\t\t12345\nJob Title:\t\tSDET"); //Second solution

    }

}
