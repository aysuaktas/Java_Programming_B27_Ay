package day03_variables;

public class _04WorldCup {
    public static void main(String[] args) {

        int year = 2018;
        System.out.println("The World Cup in " + year + " took place in Russia");

        year = 2022; // we reassigned the value // ((reassign yaparken int year = 2022 demiyoruz, sadece year = 2022 diyoruz))
        System.out.println("The World Cup in " + year + " will take place in Qatar");

        year = 2026;
        System.out.println("The World Cup in " + year + " is being planned in North America");

    }
}
