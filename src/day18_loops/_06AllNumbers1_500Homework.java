package day18_loops;

public class _06AllNumbers1_500Homework {

    public static void main(String[] args) {

        /*
        write a program that will add all the numbers from 1 to 500
         */

        int number = 1;  // we want it to start from 1.

        while(number <= 500){
            System.out.println(number);
            number++;
        }





    }
}
