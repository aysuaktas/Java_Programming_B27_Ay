package day38_encapsulation._03_rectangle;

public class _04Rectangle {

    private double length;
    private double width;

    // private'a bu class disindan da ulasmak icin -> getter & setter method olusturalim:


    // getter for length:
    public double getLength() {
        return length;
    }

    // setter for length:
    public void setLength(double length) {
        this.length = length;
    }

    // getter for width:
    public double getWidth() {
        return width;
    }

    // setter for width
    public void setWidth(double width) {
        this.width = width;
    }

}
