package replit_aysu._5_Arrays;

public class _01CreateArray {

    /*
    Create an int [] that has the values of 34, 59, 19 and print each element on a new line with the following format:

    First element: 34
    Middle element: 59
    Last element: 19
     */

    public static void main(String[] args) {

        int[] values = {34, 59, 19};

        System.out.println("First element: " + values[0]);
        System.out.println("Middle element: " + values[1]);
        System.out.println("Last element: " + values[2]);


    }
}
